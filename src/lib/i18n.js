export function t(lang, key) {
  return (translations[lang] || translations.ru)?.[key] || translations.ru[key] || key;
}

export function timeAgo(ts, lang) {
  const s = Math.floor((Date.now() - new Date(ts).getTime()) / 1000);
  if (s < 60) return t(lang, 'now');
  if (s < 3600) return Math.floor(s / 60) + ' ' + t(lang, 'min');
  if (s < 86400) return Math.floor(s / 3600) + ' ' + t(lang, 'hr');
  return Math.floor(s / 86400) + ' ' + t(lang, 'dy');
}

